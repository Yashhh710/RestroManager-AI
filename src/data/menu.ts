// Helper functions
export const getMenuByCategory = (category: string): MenuItem[] => {
  return menuItems.filter((item) => item.category === category);
};

export const getMenuCategories = (): string[] => {
  return [...new Set(menuItems.map((item) => item.category))];
};

export const getMenuItemById = (id: number): MenuItem | undefined => {
  return menuItems.find((item) => item.id === id);
};

export const getTopRatedItems = (limit: number = 10): MenuItem[] => {
  return [...menuItems].sort((a, b) => b.rating - a.rating).slice(0, limit);
};

export const getMostOrderedItems = (limit: number = 10): MenuItem[] => {
  return [...menuItems].sort((a, b) => b.orders - a.orders).slice(0, limit);
};

export const getVegetarianItems = (): MenuItem[] => {
  return menuItems.filter((item) => item.vegetarian);
};

export const getNonVegetarianItems = (): MenuItem[] => {
  return menuItems.filter((item) => !item.vegetarian);
};

export default menuItems;
