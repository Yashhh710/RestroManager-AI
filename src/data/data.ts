const getUtcDate = (date: Date): Date => {
  return new Date(Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()));
};

const formatIsoDate = (date: Date): string => {
  return date.toISOString().slice(0, 10);
};

const getDisplayDate = (isoDate: string): string => {
  const date = new Date(`${isoDate}T00:00:00Z`);
  return new Intl.DateTimeFormat('en-GB', {
    weekday: 'long',
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  }).format(date);
};

const sortedRawKeys = Object.keys(rawRestaurantData).sort((a, b) => new Date(a).getTime() - new Date(b).getTime());
const rawDataEntries = sortedRawKeys.map((key) => rawRestaurantData[key]);

const todayUtc = getUtcDate(new Date());
const latestIndex = rawDataEntries.length - 1;

const getDynamicIsoDate = (index: number): string => {
  const dynamicDate = new Date(todayUtc);
  dynamicDate.setUTCDate(dynamicDate.getUTCDate() - (latestIndex - index));
  return formatIsoDate(dynamicDate);
};

export const restaurantData: RestaurantData = rawDataEntries.reduce((acc, entry, index) => {
  const dynamicDate = getDynamicIsoDate(index);
  acc[dynamicDate] = {
    ...entry,
    date: dynamicDate,
    displayDate: getDisplayDate(dynamicDate),
  };
  return acc;
}, {} as RestaurantData);

const availableDates = Object.keys(restaurantData);
const latestDynamicDate = availableDates[availableDates.length - 1];

export const getDataForDate = (date: string): DayData => {
  return restaurantData[date] || restaurantData[latestDynamicDate];
};

export const getAvailableDates = (): string[] => {
  return availableDates;
};

// Dynamically derives a relative label ("Today", "Yesterday", "N days ago")
// for any dataset date, purely from the system clock — never hardcoded.
export const getRelativeDateLabel = (isoDate: string): string => {
  const target = new Date(`${isoDate}T00:00:00Z`);
  const current = getUtcDate(new Date());
  const diffDays = Math.round((current.getTime() - target.getTime()) / (1000 * 60 * 60 * 24));

  if (diffDays === 0) return 'Today';
  if (diffDays === 1) return 'Yesterday';
  if (diffDays > 1) return `${diffDays} days ago`;
  // Future-dated entries (shouldn't normally occur) fall back to the full date
  return getDisplayDate(isoDate);
};

// Combines the relative label with the full calendar date, e.g. "Today, 12 July 2026"
export const getRelativeDateWithFull = (isoDate: string): string => {
  const relative = getRelativeDateLabel(isoDate);
  const full = new Intl.DateTimeFormat('en-GB', {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  }).format(new Date(`${isoDate}T00:00:00Z`));
  return relative === getDisplayDate(isoDate) ? relative : `${relative}, ${full}`;
};

// Always returns today's system date in ISO form (yyyy-mm-dd) — used anywhere
// the app needs "today" without hardcoding it.
export const getTodayIsoDate = (): string => {
  return formatIsoDate(getUtcDate(new Date()));
};

// Helper function to format currency
export const formatCurrency = (value: number): string => {
  return new Intl.NumberFormat('en-IN', {
    style: 'currency',
    currency: 'INR',
    maximumFractionDigits: 0,
  }).format(value);
};

